# 데이터프레임에서 '성적'이 90점 이상이고, '출신'이 '경기'인 데이터를 조회

import pandas as pd
import numpy as np

ipsa = {
    'id': ['20231234', '20231235', '20231236', '20231237', '20231238'],
    '이름': ['차은우', '송혜교', '지창욱', '박서준', '아이유'],
    '성적': [95, 96, 92, 87, 33],
    '출신': ['서울', '경기', '경기', '강원', '제주']
}

df = pd.DataFrame(ipsa)
print("전체 데이터:")
print(df)

# 성적 90점 이상이고, 출신이 '경기'인 데이터 조회
result = df[(df['성적'] >= 90) & (df['출신'] == '경기')]
print("\n성적 90점 이상이고, 출신이 '경기'인 데이터:")
print(result)
