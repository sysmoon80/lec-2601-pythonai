"""
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
해보기 4: BMI(체질량지수) 계산 프로그램 (if-elif-else문)
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

【문제】
BMI(체질량지수) 계산 프로그램을 만들어봅시다.
사용자의 몸무게와 키를 입력받아 계산하고, 그 수치에 따라 건강 상태를 진단합니다.

【BMI 계산 공식】
BMI = 몸무게(kg) / (키(m) ** 2)

【조건】
- 0~18.5: 저체중
- ~23: 정상
- ~25: 과체중
- 그외: 비만

【실행 예시】
키를 cm 단위로 입력하세요: 160
몸무게를 kg 단위로 입력하세요: 60

당신의 BMI 지수는 23.44입니다.
진단 결과: 과체중

키를 cm 단위로 입력하세요: 180
몸무게를 kg 단위로 입력하세요: 70

당신의 BMI 지수는 21.60입니다.
진단 결과: 정상
"""

# 사용자로부터 키(cm)와 몸무게(kg) 입력받기
height = float(input("키를 cm 단위로 입력하세요: "))
weight = float(input("몸무게를 kg 단위로 입력하세요: "))

# BMI 계산 공식 (몸무게 / 키(m)의 제곱)
height_m = height / 100
bmi = weight / (height_m ** 2)

# 결과 출력 (소수점 둘째 자리까지)
print(f"\n당신의 BMI 지수는 {bmi:.2f}입니다.")

# 상태 판별
if bmi < 18.5:
    result = "저체중"
elif bmi < 23:
    result = "정상"
elif bmi < 25:
    result = "과체중"
else:
    result = "비만"

print(f"진단 결과: {result}")
