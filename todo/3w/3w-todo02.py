#!/usr/bin/env python3
"""
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
해보기 2: 나이와 운전면허 여부에 따른 운전 가능 여부 판별 (중첩 if문)
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

【문제】
다음 코드는 나이와 운전면허 여부에 따라 운전을 할 수 있는지 판별하는 문제이다.
다음의 조건에 따라 출력 내용을 다르게 하시오.

【조건】
- 나이 18살 미만인 경우: '미성년자입니다.'
- 나이 18살 이상인 경우: '성인입니다.'
  - 운전면허가 있으면: '운전을 할 수 있습니다.'
  - 운전면허가 없으면: '운전면허가 없으므로 운전을 할 수 없습니다.'

【알고리즘】
1. 나이를 입력 받는다.
2. 조건문을 이용하여 나이가 18 이상인지 비교한다.
3. 조건을 만족할 경우 '성인입니다.'를 출력한다.
4. 운전면허 소유 유무에 대한 입력을 (예/아니오)로 입력 받는다.
5. 조건을 만족하는 경우 '운전을 할 수 있습니다.'를 출력한다.
6. 만족하지 않는 경우 '운전면허가 없으므로 운전을 할 수 없습니다.'를 출력한다.
7. 나이가 18세 미만인 경우 '미성년자입니다.'를 출력한다.

【실행 예시】
나이를 입력하세요: 19
성인입니다.
운전면허를 가지고 있습니까? (예/아니오): 예
운전을 할 수 있습니다!

나이를 입력하세요: 16
미성년자입니다.
"""

# 나이 입력 받기
age = int(input("나이를 입력하세요: "))

# 나이에 따른 분기
if age >= 18:
    print("성인입니다.")
    # 운전면허 소유 여부 입력
    permission = input("운전면허를 가지고 있습니까? (예/아니오): ")
    if permission == "예":
        print("운전을 할 수 있습니다!")
    else:
        print("운전면허가 없으므로 운전을 할 수 없습니다.")
else:
    print("미성년자입니다.")
