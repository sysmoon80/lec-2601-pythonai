#!/usr/bin/env python3
"""
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
해보기 3: 미세먼지 농도에 따른 상태 판별 (if-elif-else문)
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

【문제】
미세먼지 농도에 따라 "좋음", "보통", "나쁨" 상태를 판별하시오.

【조건】
- 0~30: 좋음
- 31~80: 보통
- 81~150: 나쁨
- 151~: 매우나쁨(외출 금지)

【실행 예시】
미세먼지 : 70
현재 미세먼지 농도는 70이며, 상태는 '보통'입니다.

미세먼지 : 180
현재 미세먼지 농도는 180이며, 상태는 '매우 나쁨(외출 금지)'입니다.
"""

# 미세먼지 농도 입력 받기
dust = int(input("미세먼지 : "))  # 미세먼지 농도 (㎍/㎥)

# 미세먼지 농도에 따른 상태 판별
if dust <= 30:
    status = "좋음"
elif dust <= 80:
    status = "보통"
elif dust <= 150:
    status = "나쁨"
else:
    status = "매우 나쁨(외출 금지)"

print(f"현재 미세먼지 농도는 {dust}이며, 상태는 '{status}'입니다.")
