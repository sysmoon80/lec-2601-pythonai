#!/usr/bin/env python3
"""
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
해보기 5: 나이와 운전면허 여부를 논리연산자(and)로 한 번에 판별
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

【문제】
(해보기 2)에서 중첩 if로 사용한 문제를 간단히 작성해봅시다.
나이와 운전면허 여부를 입력받아 한 번에 운전 가능 여부를 판별하는 프로그램 만들기

【조건】
- 나이가 18세 이상이고 운전면허가 있으면: '운전을 할 수 있습니다.'
- 그 외의 경우: '운전을 할 수 없습니다.'

【힌트】
- 논리연산자 and를 사용하여 두 조건이 모두 참인지 확인합니다.
- and 연산자: 두 조건이 모두 True일 때만 True

【실행 예시】
나이를 입력하세요: 30
운전면허를 가지고 있습니까? (예/아니오): 예
운전을 할 수 있습니다!

나이를 입력하세요: 25
운전면허를 가지고 있습니까? (예/아니오): 아니오
운전을 할 수 없습니다.
"""

# 나이 입력 받기
age = int(input("나이를 입력하세요: "))

# 운전면허 소유 여부 입력
has_license = input("운전면허를 가지고 있습니까? (예/아니오): ").strip()

# 논리연산자 and를 사용하여 두 조건이 모두 만족하는지 확인
if age >= 18 and has_license == "예":
    print("운전을 할 수 있습니다!")
else:
    print("운전을 할 수 없습니다.")
