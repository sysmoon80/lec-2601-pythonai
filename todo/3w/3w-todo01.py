#!/usr/bin/env python3
"""
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
해보기 1: 주민번호 8번째 숫자로 성별 판별 (단순 if-else문)
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

【문제】
주민번호 13자리 중 8번째 숫자를 입력 받아 1, 3 이면 '남자'를 출력하고,
2, 4 이면 '여자'를 출력하시오.

【조건】
- 입력: 주민번호의 8번째 숫자 (정수)
- 출력: '남자!' 또는 '여자!'

【힌트】
- 1, 3은 홀수이고, 2, 4는 짝수입니다.
- % (나머지 연산자)를 사용하여 홀수/짝수를 판별할 수 있습니다.

【실행 예시】
주민번호의 8번째 숫자를 입력하세요: 1
남자!

주민번호의 8번째 숫자를 입력하세요: 2
여자!
"""

# 주민번호의 8번째 숫자 입력 받기
number = int(input('주민번호의 8번째 숫자를 입력하세요: '))

# 홀수(1, 3)이면 남자, 짝수(2, 4)이면 여자
if (number % 2) == 1:
    print('남자!')
else:
    print('여자!')
