#!/usr/bin/env python3
"""
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
해보기 6: 세 과목 점수 평균 계산 및 합격/불합격 판별 (논리연산자 or)
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

【문제】
사용자로부터 세 과목의 점수를 입력 받아 평균을 계산하고,
과목별 점수와 평균 점수에 따라 다음 조건을 만족하는 프로그램을 작성하시오.

【조건】
- 평균 점수가 90점 이상: "합격(우수)"
- 평균 점수가 70~89점: "합격"
- 평균 점수가 70점 미만: "불합격"
- 단, 한 과목이라도 50점 미만이면 무조건 "불합격" (과락)

【힌트】
- 논리연산자 or를 사용하여 과락 여부를 확인합니다.
- or 연산자: 조건 중 하나라도 True이면 True
- 과락 확인을 평균 점수 판별보다 먼저 해야 합니다.

【실행 예시】
첫 번째 과목 점수를 입력하세요: 80
두 번째 과목 점수를 입력하세요: 90
세 번째 과목 점수를 입력하세요: 100
결과: 합격(우수) (평균 점수: 90.00)

첫 번째 과목 점수를 입력하세요: 100
두 번째 과목 점수를 입력하세요: 100
세 번째 과목 점수를 입력하세요: 40
결과: 불합격 (과락 발생)
"""

# 세 과목 점수 입력 받기
score1 = int(input("첫 번째 과목 점수를 입력하세요: "))
score2 = int(input("두 번째 과목 점수를 입력하세요: "))
score3 = int(input("세 번째 과목 점수를 입력하세요: "))

# 평균 계산
average = (score1 + score2 + score3) / 3

# 논리연산자 or를 사용하여 과락 여부 확인
if score1 < 50 or score2 < 50 or score3 < 50:
    print("결과: 불합격 (과락 발생)")
elif average >= 90:
    print(f"결과: 합격(우수) (평균 점수: {average:.2f})")
elif average >= 70:
    print(f"결과: 합격 (평균 점수: {average:.2f})")
else:
    print(f"결과: 불합격 (평균 점수: {average:.2f})")
