#!/usr/bin/env python3
"""
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
해보기 1: 리스트의 순차 데이터를 활용한 점수 합계 계산 (for문 기초)
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

【문제】
리스트에 저장된 점수들의 합계를 계산하는 프로그램을 작성하시오.

【조건】
- 리스트: score = [70, 80, 90, 60]
- 합계를 저장할 변수 'total' 생성
- for문을 사용하여 리스트의 모든 요소를 더함
- 합계 출력

【실행 예시】
점수 합계: 300
"""

# 점수 리스트
score = [70, 80, 90, 60]

# 합계를 저장할 변수
total = 0

# for문을 사용하여 합계 계산
for s in score:
    total += s

print(f"점수 합계: {total}")
