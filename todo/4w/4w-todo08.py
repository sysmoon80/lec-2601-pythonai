#!/usr/bin/env python3
"""
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
해보기 8: 숫자 찾기 (break문)
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

【문제】
리스트에서 목표 숫자를 찾으면 프로그램을 종료하는 코드를 작성하시오.

【조건】
- 숫자 리스트: [10, 25, 40, 7, 99, 56, 33]
- 목표 숫자: 7
- break문 사용하여 루프 탈출

【실행 예시】
현재 확인 중인 숫자: 10
다음 숫자로 넘어갑니다.
현재 확인 중인 숫자: 25
다음 숫자로 넘어갑니다.
현재 확인 중인 숫자: 40
다음 숫자로 넘어갑니다.
현재 확인 중인 숫자: 7
목표 숫자 7을(를) 찾았습니다! 프로그램을 종료합니다.
프로그램이 정상적으로 종료되었습니다.
"""

# 숫자 리스트
numbers = [10, 25, 40, 7, 99, 56, 33]

# 목표 숫자
target = 7

# 숫자 찾기
for n in numbers:
    print(f"현재 확인 중인 숫자: {n}")
    if n == target:
        print(f"목표 숫자 {target}을(를) 찾았습니다! 프로그램을 종료합니다.")
        break  # 루프를 즉시 탈출합니다.
    print("다음 숫자로 넘어갑니다.")

print("프로그램이 정상적으로 종료되었습니다.")
