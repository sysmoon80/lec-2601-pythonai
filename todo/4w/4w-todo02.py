"""
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
해보기 2: 500과 1000 사이에 있는 홀수의 합계 계산 (range 함수)
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

【문제】
500과 1000 사이에 있는 홀수의 합계를 계산하는 프로그램을 작성하시오.

【조건】
- range 함수 사용 (시작: 501, 끝: 1001, 증가: 2)
- for문을 사용하여 합계 계산

【실행 예시】
500과 1000 사이에 있는 홀수의 합계 : 187500
"""

# 변수 초기화
i, total = 0, 0

# 501부터 1000까지 2간격(홀수)으로 합계 계산
for i in range(501, 1001, 2):
    total += i

print("500과 1000 사이에 있는 홀수의 합계 : %d" % total)
