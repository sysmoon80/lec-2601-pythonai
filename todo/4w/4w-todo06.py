"""
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
해보기 6: 합격 학생 이름 추출 (조건부 리스트 추가)
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

【문제】
점수 리스트에서 70점 이상인 학생의 이름만 추출하여 새 리스트에 저장하시오.

【조건】
- 점수 리스트: score = [85, 42, 77, 59, 94, 30, 65]
- 합격 학생 리스트: pass_list = []
- enumerate를 사용하여 인덱스와 점수 함께 확인
- 70점 이상인 점수만 pass_list에 추가

【실행 예시】
원본 점수: [85, 42, 77, 59, 94, 30, 65]
합격 점수: [85, 77, 94]
"""

# 점수 리스트
score = [85, 42, 77, 59, 94, 30, 65]

# 합격 학생 리스트
pass_list = []

# for문과 enumerate 사용
for idx, s in enumerate(score):
    if s >= 70:
        pass_list.append(s)

print(f"원본 점수: {score}")
print(f"합격 점수(70점 이상): {pass_list}")
