#!/usr/bin/env python3
"""
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
해보기 4: 오른쪽 정렬 별 찍기 (중첩 for문)
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

【문제】
오른쪽으로 정렬된 별 패턴을 출력하는 프로그램을 작성하시오.

【조건】
- 중첩 for문 사용
- 바깥쪽 for문: 행(1~5)
- 안쪽 for문: 공백 출력

【실행 예시】
    *
   **
  ***
 ****
*****
"""

# 오른쪽 정렬 별 찍기
for i in range(1, 6):
    # 공백 출력 (5-i개)
    for j in range(5 - i):
        print(" ", end="")
    # 별 출력 (i개)
    for k in range(i):
        print("*", end="")
    print()
