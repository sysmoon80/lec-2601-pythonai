#!/usr/bin/env python3
"""
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
해보기 3: 사용자 입력에 따른 합계 계산 (range 함수 응용)
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

【문제】
시작값, 끝값, 증가값을 입력받아 그 사이의 숫자 합계를 계산하는 프로그램을 작성하시오.

【조건】
- 시작값, 끝값, 증가값을 순서대로 입력 받음
- range 함수 사용
- 합계 출력

【실행 예시】
시작값을 입력하세요 : 1
끝값을 입력하세요 : 1000
증가값을 입력하세요 : 2
1과 1000사이에 있는 2간격의 합계 : 250000
"""

# 변수 초기화
i, total = 0, 0
start, end, step = 0, 0, 0

# 사용자 입력
start = int(input("시작값을 입력하세요 : "))
end = int(input("끝값을 입력하세요 : "))
step = int(input("증가값을 입력하세요 : "))

# range를 사용한 합계 계산
for i in range(start, end + 1, step):
    total += i

print("%d과 %d사이에 있는 %d간격의 합계 : %d" % (start, end, step, total))
