#!/usr/bin/env python3
"""
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
해보기 9: 약수 구하기 (for문 응용)
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

【문제】
입력받은 숫자의 모든 약수를 출력하고 그 합계를 계산하는 프로그램을 작성하시오.

【조건】
- 하나의 정수를 입력받음
- 1부터 해당 숫자까지 반복
- 나누어 떨어지면 약수
- 약수들을 한 줄에 출력
- 약수들의 합계 출력

【실행 예시】
약수를 구할 숫자를 입력하세요: 12
12의 약수들은 다음과 같습니다.
1 2 3 4 6 12
12의 약수들의 합은 28
"""

# 합계 변수
total = 0

# 숫자 입력
n = int(input("약수를 구할 숫자를 입력하세요: "))

print(f'{n}의 약수들은 다음과 같습니다.')

# 약수 찾기
for i in range(1, n + 1):
    if n % i == 0:
        print(i, end=' ')
        total += i

print(f'\n{n}의 약수들의 합은 {total}')
